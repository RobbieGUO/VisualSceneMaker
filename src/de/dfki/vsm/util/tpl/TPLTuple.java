package de.dfki.vsm.util.tpl;

/**
 * @author Gregor Mehlmann
 */
public class TPLTuple<F, S> {
    private F mFirst;
    private S mSecond;

    ////////////////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////
    public TPLTuple(final F first, final S second) {
        mFirst  = first;
        mSecond = second;
    }

    ////////////////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////
    public F getFirst() {
        return mFirst;
    }

    ////////////////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////
    public void setFirst(final F value) {
        mFirst = value;
    }

    ////////////////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////
    public S getSecond() {
        return mSecond;
    }

    ////////////////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////
    public void setSecond(final S value) {
        mSecond = value;
    }
}
