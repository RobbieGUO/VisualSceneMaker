package de.dfki.vsm.model.scenescript;

/**
 * @author Gregor Mehlmann
 */
public abstract class UtteranceElement extends SceneEntity {

    ////////////////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////
    public UtteranceElement() {}

    ////////////////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////
    public UtteranceElement(final int lower, final int upper) {

        // Initialize Boundary
        super(lower, upper);
    }

    ////////////////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////
    @Override
    public abstract UtteranceElement getCopy();
}
