package de.dfki.vsm.model.script;

/**
 * @author Gregor Mehlmann
 */
public abstract class AbstractWord extends SceneEntity {

    ////////////////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////
    public AbstractWord() {
    }

    ////////////////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////
    public AbstractWord(
            final int lower,
            final int upper) {
        // Initialize Boundary
        super(lower, upper);
    }

    ////////////////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////
    @Override
    public abstract AbstractWord getCopy();
}
