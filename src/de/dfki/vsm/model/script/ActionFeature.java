package de.dfki.vsm.model.script;

import de.dfki.vsm.util.ios.IndentWriter;
import de.dfki.vsm.util.xml.XMLParseError;
import de.dfki.vsm.util.xml.XMLWriteError;
import java.util.HashMap;
import org.w3c.dom.Element;

/**
 * @author Gregor Mehlmann
 */
public class ActionFeature extends SceneEntity {

    // The Key Of The Member
    protected String mKey;
    // The Value Of The Member
    protected String mVal;

    ////////////////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////
    public ActionFeature() {
    }

    ////////////////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////
    public ActionFeature(
            final int lower,
            final int upper,
            final String key,
            final String val) {
        super(lower, upper);
        // Initialize The Members
        mKey = key;
        mVal = val;
    }

    ////////////////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////
    public final String getKey() {
        return mKey;
    }

    ////////////////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////
    public final void setKey(final String key) {
        mKey = key;
    }

    ////////////////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////
    public final String getVal() {
        return mVal;
    }

    ////////////////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////
    public final void setVal(final String value) {
        mVal = value;
    }

    ////////////////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////
    @Override
    public String getText() {
        return mKey + "=" + mVal;
    }

    ////////////////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////
    @Override
    public String getText(final HashMap<String, String> args) {
        return mKey + "=" + mVal;
    }

    ////////////////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////
    @Override
    public void writeXML(final IndentWriter stream) throws XMLWriteError {
        stream.print("<ActionFeature "
                + "lower=\"" + mLower + "\" "
                + "upper=\"" + mUpper + "\" "
                + "key=\"" + mKey + "\" "
                + "val=\"" + mVal + "\"/>");
    }

    ////////////////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////
    @Override
    public void parseXML(final Element element) throws XMLParseError {
        // Parse The Boundary
        mLower = Integer.parseInt(element.getAttribute("lower"));
        mUpper = Integer.parseInt(element.getAttribute("upper"));
        // Parse The Members
        mKey = element.getAttribute("key");
        mVal = element.getAttribute("val");
    }

    ////////////////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////
    @Override
    public ActionFeature getCopy() {
        return new ActionFeature(mLower, mUpper, mKey, mVal);
    }
}
